// Ejercicio 5: Area de un Rectangulo
// Declara una funcion llamada "calcularAreaRectangulo" que acepte dos parametros, "ancho" y "alto", y retorne el Area del rectangulo

function calcularAreaRectangulo(ancho, alto) {
    return ancho * alto;
}

